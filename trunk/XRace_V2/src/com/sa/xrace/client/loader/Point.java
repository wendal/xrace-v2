package com.sa.xrace.client.loader;
public class Point {
	private float x;
	private float y;
	private float z;
	private float angle;
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	public float getZ() {
		return z;
	}
	public void setZ(float z) {
		this.z = z;
	}
	public float getAngle() {
		return angle;
	}
	public void setAngle(float andle) {
		this.angle = andle;
	}
}
